#include "3-calc.h"

/**
 * op_add - returns sum of two nums
 * @a: number
 * @b: number
 *
 * Return: expression result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns diff of 2 nums
 * @a: number
 * @b: number
 *
 * Return: expression result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns prod of 2 nums
 * @a: number
 * @b: number
 *
 * Return: expression result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns quotient of 2 nums
 * @a: number
 * @b: number
 *
 * Return: expression result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns remainder of div
 * @a: number
 * @b: number
 *
 * Return: expression result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
